#include <stdio.h>
/**
 * main - Prints the name of the program
 * @argc: The number of arguments (unused here)
 * @argv: An array of strings containing the program name and arguments
 *
 * Return: 0 (success)
 */
int main(int argc __attribute__((unused)), char *argv[])
{
printf("%s\n", argv[0]);
return (0);
}
